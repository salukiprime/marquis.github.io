/**
 * Получить текущий язык из localStorage или использовать 'en' по умолчанию
 */
function getCurrentLang() {
    return localStorage.getItem('marquis_lang') || 'en';
}

/**
 * Установить новый язык и перезагрузить страницу для применения
 */
function setLanguage(langCode) {
    if (langCode === 'en' || langCode === 'ru') {
        localStorage.setItem('marquis_lang', langCode);
        window.location.reload();
    }
}

/**
 * Вспомогательная функция для получения переведенной строки по ключу
 */
function t(key) {
    const lang = getCurrentLang();
    if (translations[key] && translations[key][lang]) {
        return translations[key][lang];
    }
    return key; // Возврат ключа, если перевод не найден
}

/**
 * Применить переводы ко всем DOM-элементам с атрибутом data-i18n
 */
function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        
        // Специфическая обработка плейсхолдеров (заполнителей)
        if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
             el.setAttribute('placeholder', t(key));
        } else {
             // Если innerHTML содержит другие теги, будьте осторожны.
             // Обычно data-i18n размещается на конечных узлах.
             el.innerHTML = t(key);
        }
    });

    // Настройка интерфейса переключателя языка
    const langToggleContainer = document.querySelector('.lang');
    if (langToggleContainer) {
        const currentLang = getCurrentLang();
        const otherLang = currentLang === 'en' ? 'RU' : 'EN';
        const otherLangCode = currentLang === 'en' ? 'ru' : 'en';

        langToggleContainer.innerHTML = `
            <span style="font-weight: ${currentLang === 'en' ? 'bold' : 'normal'}; color: ${currentLang === 'en' ? 'var(--color-white)' : 'var(--color-creme-dark)'}">EN</span>
            <span style="margin: 0 4px; color: #555">|</span>
            <span style="font-weight: ${currentLang === 'ru' ? 'bold' : 'normal'}; color: ${currentLang === 'ru' ? 'var(--color-white)' : 'var(--color-creme-dark)'}; cursor: pointer;" onclick="setLanguage('${otherLangCode}')">RU</span>
        `;
        
        // Сделать оба пункта кликабельными для удобства
        langToggleContainer.innerHTML = `
            <span onclick="setLanguage('en')" style="cursor: pointer; font-weight: ${currentLang === 'en' ? 'bold' : 'normal'}; color: ${currentLang === 'en' ? 'var(--color-white)' : '#888'}">EN</span>
            <span style="margin: 0 4px; color: #555">|</span>
            <span onclick="setLanguage('ru')" style="cursor: pointer; font-weight: ${currentLang === 'ru' ? 'bold' : 'normal'}; color: ${currentLang === 'ru' ? 'var(--color-white)' : '#888'}">RU</span>
        `;
    }
}

// Автозапуск после загрузки DOM
document.addEventListener('DOMContentLoaded', applyTranslations);
